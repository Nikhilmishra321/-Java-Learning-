package Operator;

public class Assignment {
    public static void main(String[] args) {
        //Assignment Operator

        //Decleraing variable

        int num;
        String name;

        //Assign value

        num =10;
        name="Tony Stark";

        //Displaying value

        System.out.println("num is assigned" + num);
        System.out.println("name is assigned" + name);

        
    }
    
}

public class ProductOfNumber {
    public static void main(String[] args) {
        int a =10;
        int b =20;
        int prod=multiply(a, b);
        System.out.println("multiply of two number" + prod);
        
    }

    public static int multiply(int a, int b){
        int product=a * b;
        return product;

    }

    
}
